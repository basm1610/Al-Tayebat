class MenuItem {
  final String name;
  final String description;
  final double price;
  final double rating;
  final String imageUrl;
  final String prepTime;
  final String category;

  const MenuItem({
    required this.name,
    required this.description,
    required this.price,
    required this.rating,
    required this.imageUrl,
    required this.prepTime,
    required this.category,
  });
}
